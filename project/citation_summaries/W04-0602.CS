In the future we plan to include the AMI Corpus metadata in public catalogues, through the Open (Language) Archives Initiatives network (Bird and Simons, 2001), as well as through the IMDI network (Wittenburg et al. , 2004).


The standard chosen for the explicit metadata files is the IMDI format, proposed by the ISLE Meta Data Initiative (Wittenburg et al. , 2002; Broeder et al. , 2004a) (see http://www.mpi.nl/IMDI/tools), which is precisely intended to describe multimedia recordings of dialogues.


