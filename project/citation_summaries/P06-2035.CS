The human language technology (HLT) society in Europe has been particularly zealous for the standardization, making a series of attempts such as EAGLES 3 , PROLE/SIMPLE (Lenci et al., 2000), ISLE/MILE (Calzolari et al., 2003; Bertagna et al., 2004) and more recently multilingual lexical database generation from parallel texts in 20 European languages (Giguet and Luquet, 2006).


