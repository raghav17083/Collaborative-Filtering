The MCextension allows for linguistically satisfying accounts for a number of attested phenomena, such as: English extraposition (Kroch and Joshi 1986), subj-aux inversion in combination with raising verbs (Frank 1992), anaphoric binding (Ryant and Scheffler 2006), quantifier scope ambiguity (Joshi et al. 2003), clitic climbing in Romance (Bleam 1994), and Japanese causatives (Heycock 1986).


