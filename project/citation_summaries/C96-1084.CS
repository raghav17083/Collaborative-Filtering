Also, the provision of conceptual entities which are incrementally generated by the semantic interpretation process supplies the necessary anchoring points for the continuous resolution of textual anaphora and ellipses (Strube & Hahn, 1995; Hahn et al. , 1996).


Bridging anaphora (Clark, 1975) represents a special part of the general problem of anaphora resolution, which has been studied and discussed for various languages and domains (Hahn et al., 1996; Murata et al., 1999; Poesio et al., 2004; Gasperin and Vieira, 2004; Gasperin and Briscoe, 2008).


Our work on the resolution of anaphora (Strube & Hahn, 1995; Hahn & Strube, 1996) and textual ellipsis (Hahn et al. , 1996), however, is based on German, a free word order language, in which grammatical role information is far less predictive for the organization of centers.


Work which addressed indirect anaphora in English texts so far restricts relationships to a small, relatively well-defined set, mainly part-of relation like the above example (2), and utilized hand-crafted heuristic rules or hand-crafted lexical knowledge such as WordNet (Hahn et al. , 1996; Vieira and Poesio, 2000; Strube and Hahn, 1999).


