One of the crucial points is that tagged text should be represented by FSA in order to be disambiguated (disambiguated texts are already in this form in Rimon and Herz 1991 and K. Koskenniemi 1990).


Restricting the system that way makes our approach to context-sensitiveness different from the one considered in (Rimon and Herz, 1991), where context sensitive spelling verification is proposed to be done with the help of "local constraints automata (LCAs)" which process contextual constraints on the level of lexical or syntactic categories rather than on the basic level of strings.


Under certain circumstances, this may be done by carrying over the probabilities from an input probabilistic CFG (PCFG), as shown for the special case of n-grams by (Rimon and Herz, 1991; Stolcke and Segal, 1994), or by training of the FA on a corpus generated by the PCFG (Jurafsky et al. , 1994).


The special case of n-grams has already been formulated by Stolcke and Segal (1994), realizing an idea previously envisioned by Rimon and Herz (1991).


