Urdu contains 10 vowels and 7 of them have nasalized forms (Hussain, 2004; Khan, 1997).


For some languages such as English and French, the relationship is complex and require large numbers of rules (El-Imam and Don, 2005; Damper et al. , 1998), while some languages such as Urdu (Hussain, 2004), and Hindi (Ramakishnan et al. , 2004; Choudhury, 2003) show regular behavior and thus pronunciation can be modeled by defining fairly regular simple rules.


