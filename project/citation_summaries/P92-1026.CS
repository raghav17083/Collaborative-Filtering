EFOG is also related to FO-TAG (Becker et al. 1991) and the HPSG approach (Engelkamp et al. 1992, Oliva 1992) in extending the domain of applicability of LP rules.


Sporadic counter-evidence for ECPO grammars has been found for some languages like English (the verb-particle construction, Sag 1987, Pollard and Sag 1987), German (complex fronting, Uszkoreit 1985, Engelkamp et aL 1992) and Finnish (the adverb my(is 'also, too' Zwicky and Nevis 1986).


