Root forms in the lexicon are represented in a discrimination net for efficient access (cf Emirkanian and Bouchard, 1988).


Other work (e.g. Veronis, 1988, Emirkanian and Bouchard, 1988, van Berkel and De Smedt, 1988) emphasizes the former, often on the grounds that competence errors are both harder for the user to correct and tend to make a worse impression on a human reader.


