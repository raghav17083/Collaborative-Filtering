In addition to the usual release, the training and testing data were also provided in a lemmatized version (Aduriz et al. 2000) which included lemma, PoS and case information.


The procedure to find examples from the web was the following: for each target word all possible morphological declensions were automatically generated, searched in a searchengine, documents retrieved, automatically lemmatized (Aduriz et al. 2000), filtered using some heuristics to ensure quality of context, and finally filtered for PoS mismatches.


Our group used directly the output of the parser (Aduriz et al. , 2000), which includes some additional features: number, determiner mark, ambiguous analyses and elliptic words.


We tried to represent this information in local features, relying on the analysis of a deep morphological analyzer developed in our group (Aduriz et al. , 2000).


3 Linguistic Processing Tools The corpus was automatically analyzed by means of several linguistic processors: a morphosyntactic analyzer (Aduriz et al., 2000), EUSTAGGER, the lemmatizer/tagger for Basque, and the Constraint Grammar parser (CG, Tapanainen, 1996) for morphological disambiguation.


