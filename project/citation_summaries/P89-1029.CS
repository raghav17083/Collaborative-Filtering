One useful result of our work on extending the GHR algorithm to handle annotated context free grammars (ACFGs) is the discovery that there is a class of ACFGs, depth-bounded ACFGs, for which the parsing algorithm is guaranteed to find all parses and halt (Haas (1989)).


Hence, an amg is not necessarily off-line parsable ( Pereira and Warren (1983), Haas (1989)).


