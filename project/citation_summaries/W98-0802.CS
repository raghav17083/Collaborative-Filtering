4.6 SyncTool SyncTool (Nivre et al. , 1998) is developed (as a prototype for MultiTool) for synchronizing transcriptions with digitized audio/video recordings.


4.1 TransTool TransTool (Nivre et al. , 1998) is a computer tool for transcribing spoken language in accordance with the transcription standard (Nivre 1999a and b).


